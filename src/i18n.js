let currentLang = "he";

export function setLang(lang) {
  if (LANGS.includes(lang)) currentLang = lang;
  document.documentElement.lang = currentLang;
  document.documentElement.dir = DICT[currentLang].dir;
}

export function getLang() {
  return currentLang;
}

export function getDir() {
  return DICT[currentLang].dir;
}

export function t(key, params) {
  let str = DICT[currentLang][key];
  if (str === undefined) str = DICT.en[key] !== undefined ? DICT.en[key] : key;
  if (params) {
    for (const k of Object.keys(params)) {
      str = str.replace(new RegExp("\\{" + k + "\\}", "g"), params[k]);
    }
  }
  return str;
}
